    private String getValueFromJson(String jsonString, String key) {
            int keyIndex = jsonString.indexOf("\"" + key + "\":");
            int valueIndex = jsonString.indexOf("\"", keyIndex + key.length() + 3);
            int endIndex = jsonString.indexOf("\"", valueIndex + 1);

            if (valueIndex > keyIndex && valueIndex < endIndex) {
                return jsonString.substring(valueIndex + 1, endIndex);
            }

            return "";
    }
    
    private void botaoBuscaCepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscaCepActionPerformed
        String cep = campoCep.getText();
        if (cep.length() != 8 || !cep.matches("[0-9]+")) {
            //JOptionPane.showMessageDialog(null, "CEP inválido. Certifique-se de digitar um CEP válido com 8 dígitos numéricos.", "CEP Inválido", JOptionPane.ERROR_MESSAGE);
            labelErro.setText("CEP inválido. Certifique-se de digitar um CEP válido com 8 dígitos numéricos.");
            return;
        }
        try {
                String urlStr = "https://viacep.com.br/ws/" + cep + "/json/";
                URL url = new URL(urlStr);
                HttpURLConnection conn = (HttpURLConnection) url.openConnection();
                conn.setRequestMethod("GET");

                if (conn.getResponseCode() == 200) {
                    BufferedReader br = new BufferedReader(new InputStreamReader(conn.getInputStream()));
                    StringBuilder jsonText = new StringBuilder();
                    String line;

                    while ((line = br.readLine()) != null) {
                        jsonText.append(line);
                    }

                    br.close();
                    
                    // Since we're using only native libraries, we parse the JSON manually
                    String jsonString = jsonText.toString();
                    String logradouro = getValueFromJson(jsonString, "logradouro");
                    String bairro = getValueFromJson(jsonString, "bairro");
                    String localidade = getValueFromJson(jsonString, "localidade");
                    String uf = getValueFromJson(jsonString, "uf");
                    
                    System.out.println(logradouro);
                    
                    campoRua.setText(logradouro);
                    campoBairro.setText(bairro);
                    campoCidade.setText(localidade);
                    campoEstado.setText(uf);
                    
                } else {
                    labelErro.setText("CEP não encontrado. Verifique se o CEP está correto.");
                    //JOptionPane.showMessageDialog(null, "CEP não encontrado. Verifique se o CEP está correto.", "CEP não Encontrado", JOptionPane.WARNING_MESSAGE);
                }

                conn.disconnect();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao realizar a busca por CEP. Verifique sua conexão de internet.", "Erro de Conexão", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_botaoBuscaCepActionPerformed

