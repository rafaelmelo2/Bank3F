    public static boolean validaDataNascimento(String nascimento) {
        // Expressão regular para verificar o formato 'dd/mm/aaaa' com barras
        String verificacao = "^(0[1-9]|[1-2][0-9]|3[0-1])/(0[1-9]|1[0-2])/\\d{4}$";
        Pattern pattern = Pattern.compile(verificacao);
        Matcher matcher = pattern.matcher(nascimento);
        return matcher.matches();
    }
    
    
